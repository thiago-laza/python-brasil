'''Faça um Programa que peça dois números e imprima o maior deles.'''

num_1 = float(input('Digite um numero: '))
num_2 = float(input('Digite outro numero: '))

if num_1 > num_2:
    print(f'Os numeros digitados foram {num_1} e {num_2} e o maior e {num_1}')
else:
    print(f'Os numeros digitados foram {num_1} e {num_2} e o mairo e {num_2}')